// public = an access modifier that defines scope to allow other parts of the code to access this code
// class = code block(unique code to specific method) with body defined by curly braces and Name with an access modifier
public class Hello {
    // main() = the method that serves as the entry point Java looks for when running an app
    // static = an access modifier
    // void = the method (a function on a class) will not return anything
    // String[] args = a parameter that is named args and is an array of Strings
    public static void main(String[] args){

        // System.out.println() = print statement that uses double quotes
        // Java requires semicolons to end lines
        System.out.println("Hello World!");
    }

}
